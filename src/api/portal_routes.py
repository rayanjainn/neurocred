@router.get("/score/{task_id}/stream")
async def score_stream_alias(task_id: str, request: Request) -> EventSourceResponse:
    """Alias: GET /score/{task_id}/stream -> pubsub updates"""
    redis = request.app.state.redis
    
    async def event_generator():
        pubsub = redis.pubsub()
        await pubsub.subscribe(f"updates:{task_id}")
        try:
            while True:
                message = await pubsub.get_message(ignore_subscribe_messages=True, timeout=1.0)
                if message and message["type"] == "message":
                    data_str = message["data"].decode("utf-8")
                    yield {"data": data_str}
                    if '"status": "complete"' in data_str or '"status": "failed"' in data_str:
                        break
                else:
                    await asyncio.sleep(0.5)
        finally:
            await pubsub.unsubscribe()
            await pubsub.close()
            
    return EventSourceResponse(event_generator())


