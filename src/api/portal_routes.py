def _find_entity_on_disk(q: str):
    """Search for q in data/raw/user_profiles.parquet and return user dict if found."""
    p_path = Path("data/raw/user_profiles.parquet")
    if not p_path.exists():
        return None
    try:
        df = pl.read_parquet(p_path)
        q_lower = q.lower()
        # search across multiple columns (id, gstin, name, upi)
        mask = (
            (pl.col("user_id").str.to_lowercase() == q_lower) |
            (pl.col("user_id").str.to_lowercase().str.contains(q_lower)) |
            (pl.col("gstin").str.to_lowercase() == q_lower) |
            (pl.col("name").str.to_lowercase().str.contains(q_lower)) |
            (pl.col("business_name").str.to_lowercase().str.contains(q_lower)) |
            (pl.col("upi_id").str.to_lowercase() == q_lower) |
            (pl.col("vpa").str.to_lowercase() == q_lower)
        )
        # Handle masked account number search (e.g. XXXXabcd)
        if q_lower.startswith("xxxx"):
            acc_tail = q_lower[4:]
            if acc_tail:
                mask = mask | (pl.col("user_id").str.to_lowercase().str.starts_with(acc_tail))
        res = df.filter(mask).head(1)
        if res.is_empty():
            return None
        return res.to_dicts()[0]
    except Exception:
        return None

