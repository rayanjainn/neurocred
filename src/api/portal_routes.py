@router.post("/auth/login")
async def auth_login(body: dict[str, Any]) -> dict[str, Any]:
    email = str(body.get("email", "")).strip().lower()
    password = str(body.get("password", ""))
    if not email or not password:
        raise HTTPException(status_code=400, detail="Email and password are required")
    if password != "demo":
        raise HTTPException(status_code=401, detail="Invalid credentials")
    user = next((u for u in _STORE["users"] if u["email"].lower() == email), None)
    if not user:
        raise HTTPException(status_code=401, detail="User not found")
    token = f"token-{user['id']}-{int(time.time())}"
    _STORE["tokens"][token] = user["id"]
    return {"token": token, "user": user}


@router.post("/auth/logout")
async def auth_logout(request: Request) -> dict[str, Any]:
    auth = request.headers.get("Authorization", "")
    token = auth.removeprefix("Bearer ").strip()
    _STORE["tokens"].pop(token, None)
    return {"ok": True}


@router.get("/auth/me")
async def auth_me(request: Request) -> dict[str, Any]:
    user = _require_user(request)
    return user


