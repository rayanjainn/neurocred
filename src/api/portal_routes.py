def _decode_redis_hash(row: dict[Any, Any]) -> dict[str, str]:
    out: dict[str, str] = {}
    for k, v in row.items():
        key = k.decode("utf-8") if isinstance(k, bytes) else str(k)
        val = v.decode("utf-8") if isinstance(v, bytes) else str(v)
        out[key] = val
    return out


def _parse_iso_dt_or_min(raw: str | None) -> datetime:
    if not raw:
        return datetime.min.replace(tzinfo=timezone.utc)
    try:
        dt = datetime.fromisoformat(str(raw).replace("Z", "+00:00"))
        if dt.tzinfo is None:
            dt = dt.replace(tzinfo=timezone.utc)
        return dt
    except Exception:
        return datetime.min.replace(tzinfo=timezone.utc)


async def _get_live_credit_snapshot(request: Request, *identifiers: Any) -> tuple[int | None, str | None]:
    """Fetch latest score for entity aliases from scoring_worker cache in Redis."""
    redis = request.app.state.redis

    candidate_ids: list[str] = []
    for raw in identifiers:
        token = str(raw or "").strip()
        if token and token not in candidate_ids:
            candidate_ids.append(token)

    best_score: int | None = None
    best_band: str | None = None
    best_refreshed = datetime.min.replace(tzinfo=timezone.utc)

    for cid in candidate_ids:
        try:
            row = await redis.hgetall(f"credit:user:{cid}")
        except Exception:
            continue
        if not row:
            continue

        parsed = _decode_redis_hash(row)
        try:
            score = int(float(parsed.get("credit_score", "0") or 0))
        except Exception:
            score = 0
        if score <= 0:
            continue

        refreshed = _parse_iso_dt_or_min(parsed.get("refreshed_at"))
        if refreshed >= best_refreshed:
            best_refreshed = refreshed
            best_score = max(300, min(900, score))
            band = str(parsed.get("risk_band", "")).strip()
            best_band = band or None

    return best_score, best_band


@router.get("/explorer/gstins")
async def get_explorer_gstins(request: Request) -> list[dict[str, Any]]:
    # 1. Start with hardcoded system nodes (always present for demo)
    result = []
    seen = set()
    for gstin, name in _GSTIN_NAMES.items():
        score = _GSTIN_SCORES.get(gstin)
        disk_row = _find_entity_on_disk(gstin)
        disk_uid = (disk_row or {}).get("user_id")
        if score:
            score_val = int(score.get("credit_score", 700))
            band = str(score.get("risk_band", "low_risk"))
        else:
            profile_type = (disk_row or {}).get("profile_type", "HEALTHY_MSME")
            age_months = int((disk_row or {}).get("age_months", 24) or 24)
            score_val, band = _get_generated_score_and_band(profile_type, age_months, gstin)

        live_score, live_band = await _get_live_credit_snapshot(request, gstin, disk_uid)
        if live_score is not None:
            score_val = live_score
            if live_band:
                band = live_band

        profile = "FRAUD_SHELL" if band == "high_risk" else (
            "STRUGGLING_MSME" if band == "medium_risk" else "HEALTHY_MSME"
        )
        result.append({
            "gstin": gstin,
            "business_name": name,
            "profile_type": profile,
            "state_code": gstin[:2],
            "business_age_months": 12 + (ord(gstin[0]) % 36),
            "credit_score": score_val,
            "risk_band": band,
        })
        seen.add(gstin)
    
    # 2. Augment with data from raw profiles parquet (the "real" generated data)
    p_path = Path("data/raw/user_profiles.parquet")
    if p_path.exists():
        try:
            df = pl.read_parquet(p_path)
            for row in df.to_dicts():
                g = row.get("gstin")
                if not g or g in seen:
                    continue
                
                # Check for business_name OR name (crucial for shell companies)
                label = row.get("business_name") or row.get("name") or f"Entity {g[:6]}"
                
                score_val, band = _get_generated_score_and_band(row["profile_type"], row.get("age_months", 24), g)
                score_row = _GSTIN_SCORES.get(g)
                final_score = int(score_row.get("credit_score", score_val)) if score_row else score_val
                final_band = str(score_row.get("risk_band", band)) if score_row else band

                live_score, live_band = await _get_live_credit_snapshot(request, g, row.get("user_id"))
                if live_score is not None:
                    final_score = live_score
                    if live_band:
                        final_band = live_band
                
                result.append({
                    "gstin": g,
                    "business_name": label,
                    "profile_type": row["profile_type"],
                    "state_code": row.get("state_code", g[:2]),
                    "business_age_months": row.get("age_months", 24),
                    "credit_score": final_score,
                    "risk_band": final_band,
                })
                seen.add(g)
        except Exception as e:
            print(f"Error loading explorer profiles: {e}")

    return result


def _get_generated_score_and_band(profile_type: str, age_months: int, identifier: str):
    """Generate a realistic, deterministic score based on Copula-aligned persona parameters."""
    import random
    rng = random.Random(hash(identifier) & 0xFFFF)
    
    # Baseline by Persona
    if profile_type == "SHELL_CIRCULAR" or profile_type == "FRAUD_SHELL":
        base = 380; variation = 120
    elif "STRUGGLING" in profile_type:
        base = 520; variation = 150
    elif "NEW_TO_CREDIT" in profile_type:
        base = 650; variation = 80
    else: # GENUINE_HEALTHY / HEALTHY_MSME
        base = 710; variation = 140
        
    # Adjustment for business age
    age_bonus = min(50, (age_months // 6) * 5)
    
    score = int(base + (rng.random() * variation) + age_bonus)
    score = max(300, min(900, score))
    
    band = "high_risk" if score < 500 else ("medium_risk" if score < 680 else "low_risk")
    return score, band

def build_monthly_timeline(identifier: str, months: int = 12) -> list[dict]:
    """Generate a synthetic daily transaction timeline for the last `months` months."""
    import random, math
    rng = random.Random(hash(identifier) & 0xFFFF)
    score = _GSTIN_SCORES.get(identifier) or _INDIVIDUAL_SCORES.get(identifier)
    band = score["risk_band"] if score else "low_risk"
    base_upi = 60000 if band == "high_risk" else (90000 if band == "medium_risk" else 140000)
    base_ewb = int(base_upi * 0.7)
    from datetime import date, timedelta
    today = date.today()
    result = []
    for d in range(months * 30, 0, -1):
        dt = today - timedelta(days=d)
        # seasonal sinusoidal + noise
        season = 1 + 0.2 * math.sin(2 * math.pi * dt.month / 12)
        noise_u = rng.uniform(0.85, 1.18)
        noise_e = rng.uniform(0.80, 1.15)
        vol = int(base_upi * season * noise_u)
        cnt = max(5, int(20 * season * rng.uniform(0.8, 1.2)))
        eva = int(base_ewb * season * noise_e)
        evc = max(2, int(10 * rng.uniform(0.8, 1.2)))
        result.append({
            "date": dt.isoformat(),
            "daily_volume": vol,
            "daily_count": cnt,
            "daily_ewb_volume": eva,
            "daily_ewb_count": evc,
        })
    return result


def window_agg(timeline: list[dict], days: int) -> dict:
    subset = timeline[-days:]
    return {
        "days": days,
        "upi_volume": sum(t["daily_volume"] for t in subset),
        "upi_count": sum(t["daily_count"] for t in subset),
        "ewb_volume": sum(t["daily_ewb_volume"] for t in subset),
        "ewb_count": sum(t["daily_ewb_count"] for t in subset),
        "avg_daily_upi": int(sum(t["daily_volume"] for t in subset) / max(1, len(subset))),
    }


def _window_agg(timeline: list[dict], days: int) -> dict:
    """Backward-compatible alias for existing call sites."""
    return window_agg(timeline, days)


