def _get_real_transactions(identifier: str, limit: int = 50):
    """Scan raw chunks for transactions belonging to this entity. Resolves gstin <-> user_id."""
    results = []
    ewb_results = []
    
    _pipeline_log("TXN_LOOKUP", f"identifier={repr(identifier)} limit={limit}")
    # Identify target IDs (could be gstin, user_id, or vpa)
    target_ids = {identifier}
    p_path = Path("data/raw/user_profiles.parquet")
    if p_path.exists():
        try:
            df_u = pl.read_parquet(p_path)
            # Find row where identifier matches gstin, user_id, or vpa
            match = df_u.filter(
                (pl.col("gstin") == identifier) | 
                (pl.col("user_id") == identifier) | 
                (pl.col("vpa") == identifier) | 
                (pl.col("upi_id") == identifier)
            ).to_dicts()
            if match:
                target_ids.add(match[0]["user_id"])
                if match[0].get("gstin"): target_ids.add(match[0]["gstin"])
                if match[0].get("vpa"): target_ids.add(match[0]["vpa"])
                if match[0].get("upi_id"): target_ids.add(match[0]["upi_id"])
                _pipeline_log("IDENTITY_RESOLVED", f"mapped {len(target_ids)} IDs: {list(target_ids)[:3]}")
            else:
                _pipeline_log("IDENTITY_NOT_FOUND", f"no profile for {repr(identifier)} - scanning directly", level="WARNING")
        except Exception as e:
            _log.error(f"[PIPELINE] identity-map error: {e}")

    try:
        # Search UPI Chunks - Ensure no 'gstin' column is projected as it doesn't exist in UPI schema
        upi_files = list(Path("data/raw").glob("upi_transactions_chunk_*.parquet"))
        if upi_files:
            _pipeline_log("UPI_SCAN", f"scanning {len(upi_files)} parquet chunks for {len(target_ids)} IDs")
            target_list = list(target_ids)
            df_upi = pl.scan_parquet(upi_files).filter(
                pl.col("user_id").is_in(target_list)
            ).sort("timestamp", descending=True).limit(limit).collect()
            _pipeline_log("UPI_SCAN_DONE", f"found {len(df_upi)} UPI transactions")
            for row in df_upi.to_dicts():
                results.append({
                    "type": "UPI",
                    "txn_id": row.get("event_id", row.get("txn_id")),
                    "amount": row["amount"],
                    "timestamp": row["timestamp"],
                    "date": row["timestamp"], 
                    "direction": row.get("direction", "inbound"),
                    "counterparty_vpa": row.get("counterparty_upi", "unknown@upi"),
                    "merchant_category": row.get("merchant_name", "GENERAL")
                })

        # Search EWB Chunks - Contains 'gstin' and 'user_id'
        ewb_files = list(Path("data/raw").glob("eway_bills_chunk_*.parquet"))
        if ewb_files:
            _pipeline_log("EWB_SCAN", f"scanning {len(ewb_files)} e-way bill chunks")
            target_list = list(target_ids)
            df_ewb = pl.scan_parquet(ewb_files).filter(
                (pl.col("user_id").is_in(target_list)) | 
                (pl.col("gstin").is_in(target_list))
            ).sort("timestamp", descending=True).limit(limit).collect()
            _pipeline_log("EWB_SCAN_DONE", f"found {len(df_ewb)} EWB records")
            for row in df_ewb.to_dicts():
                ewb_results.append({
                    "ewb_no": row.get("eway_id", row.get("event_id")),
                    "totalValue": float(row.get("totalValue", row.get("tot_inv_value", row.get("amount", 0)))),
                    "timestamp": row.get("timestamp"),
                    "mainHsnCode": str(row.get("main_hsn_code", row.get("hsn", "N/A"))),
                    "fromGstin": row.get("from_gstin", "UNKNOWN"),
                    "toGstin": row.get("to_gstin", "UNKNOWN")
                })
        
        return results, ewb_results
    except Exception as e:
        _log.error(f"[PIPELINE] TXN_FETCH_ERROR for {identifier}: {e}")
        return [], []

